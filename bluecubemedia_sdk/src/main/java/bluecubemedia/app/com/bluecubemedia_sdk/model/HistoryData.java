public class HistoryData {
    private String from;
    private String to;
    private String amount;
    private String finial_rate;

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getFinial_rate() {
        return finial_rate;
    }

    public void setFinial_rate(String finial_rate) {
        this.finial_rate = finial_rate;
    }
}
